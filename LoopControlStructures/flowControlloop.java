package LoopControlStructures;
public class flowControlloop{
    public static void main(String[] args) {
     //while loop
     int num = 0;
     while (num <5){
         System.out.println("This is a new method");
         ++num;
     }
     
     //do while loop
     int age = 24;
     do{
        System.out.println("I refuse to age!");
        ++age;
     }while (age < 27);
    }
   
    
}
